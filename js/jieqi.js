/* 当令节气判定：返回 SOLAR_TERMS 索引 */
function currentTermIndex(date) {
  const key = (date.getMonth() + 1) * 100 + date.getDate();
  // 按公历日期升序排列
  const cal = SOLAR_TERMS
    .map((t, i) => ({ i, key: t.date[0] * 100 + t.date[1] }))
    .sort((a, b) => a.key - b.key);
  let idx = -1;
  for (const t of cal) { if (t.key <= key) idx = t.i; else break; }
  if (idx === -1) idx = cal[cal.length - 1].i;   // 1月初（小寒前）→ 上一年冬至
  return idx;
}

/* 距下一个节气的天数（粗略，用当年/跨年估算） */
function daysToNextTerm(date) {
  const cur = currentTermIndex(date);
  const next = SOLAR_TERMS[(cur + 1) % SOLAR_TERMS.length];
  const y = date.getFullYear();
  let nd = new Date(y, next.date[0] - 1, next.date[1]);
  if (nd <= date) nd = new Date(y + 1, next.date[0] - 1, next.date[1]);
  return Math.max(0, Math.ceil((nd - date) / 86400000));
}
